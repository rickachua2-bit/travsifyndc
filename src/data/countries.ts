const BY_CODE = new Map(COUNTRIES.map((c) => [c.code, c]));

export function findCountryByCode(code: string | null | undefined): Country | undefined {
  if (!code) return undefined;
  return BY_CODE.get(code.toUpperCase());
}

/** Fuzzy search by name or code; returns top N matches. */
export function searchCountries(q: string, limit = 8): Country[] {
  const query = q.trim().toLowerCase();
  if (!query) return COUNTRIES.slice(0, limit);
  const startsWith: Country[] = [];
  const contains: Country[] = [];
  for (const c of COUNTRIES) {
    const name = c.name.toLowerCase();
    if (name.startsWith(query) || c.code.toLowerCase() === query) startsWith.push(c);
    else if (name.includes(query)) contains.push(c);
    if (startsWith.length >= limit) break;
  }
  return [...startsWith, ...contains].slice(0, limit);
}
